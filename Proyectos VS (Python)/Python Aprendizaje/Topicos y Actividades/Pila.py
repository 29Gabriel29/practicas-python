pila = []
pila.append(1)
pila.append(2)
pila.append(3)
pila.append(4)
# Append agrega elementos siempre al final de la lista
print(pila)

elementoSacado = pila.pop()
# Pop siempre saca el ultimo elemento
# Quito el ultimo elemento y devuelve el elemento que estees sacando
print(pila)
print(elementoSacado)
